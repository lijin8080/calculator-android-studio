package com.lj.calc;

public class Equal {
    String a;
    String result;

    public Equal() {
    }

    public Equal(String a, String result) {
        this.a = a;
        this.result = result;
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }
}
